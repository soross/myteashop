          <div class='content'><h2>视点简介</h2>
<blockquote>
<p>&ldquo;视点网络(视点工作室)&rdquo;是一个融创意、设计、开发于一体的专业网站建设服务商，业务内容涉及企业网站建设、门户网站开发、电子商务及WEB应用系统构建。</p>
<p>我们有着丰富的实践经验、精良的专业技术和高度的敬业精神，在网络服务陷入白热化竞争的今天，我们依然遵循可用性原则，坚持为企业建设简洁大气、好用易用的网站。</p>
<p>我们的专业网站工程师在各项建站技能上均有超卓的表现，包括 HTML、Javascript、CSS、ASP 及 Databases 等等。在每一个重要的工序上，我们均会和您有紧密的联系，务使每一个细节均能尽善尽美。</p>
</blockquote>
<hr />
<h2>视点文化</h2>
<blockquote>
<p>积极主动追求卓越 <br />视点网络从不以现有的成就自满，精益求精是每位成员面对自己工作时的基本态度，也是视点网络得以不断成长的原动力。</p>
<p>重视团队合作精神 <br />视点网络以专业团队来完成每位客户的托付，团队成员彼此间的默契与合作精神是视点网络赢取客户信任以及成功的重要关键因素。</p>
<p>客户服务导向 <br />视点网络所提供的产品及服务，向来以协助客户提升网络形象及竞争优势为核心宗旨，不仅是解决客户问题的好伙伴，更是支持客户成长的忠诚力量。</p>
<p>勇于面对市场挑战 <br />视点网络兢兢业业地面对市场变化，以专业素养、坚定的毅力以及迅速的应变能力，面对市场不断的变化与挑战。</p>
</blockquote>
<hr />
<h2>服务宗旨</h2>
<blockquote>
<p>"更高、更快、更贴心" 是我们的服务宗旨，也是客户的承诺。</p>
<p>「更高」 <br />我们的专业程序工程师及富有创意的设计师均有十足信心为客户提供高品质的网站建设服务，让客户的投入物超所值！</p>
<p>「更快」 <br />急客户之所急，出色而高效率的完成客户交付的任务。</p>
<p>「更贴心」 <br />凭借我们丰富的经验，针对不同客户不同需求，给予专业意见配合他们的经营手法，用我们的创意及技术诚意地为每一位客户创建出贴心的网站，帮助客户建立良好的商业形象。</p>
